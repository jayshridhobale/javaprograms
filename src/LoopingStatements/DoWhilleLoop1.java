package LoopingStatements;

public class DoWhilleLoop1 {
    public static void main(String[] args) {
        int a=2;
        while(a>2){        //entry control
            a--;
        }
        System.out.println(a);
        int b=2;
        do{
            b--;
        }while(b>2);
        System.out.println(b);
        
    }
}
