package WrapperClass;

public class WrapperDemo2 {
    public static void main(String[] args) {
        String str=new String("jayshri");
        String str2=str;
        System.out.println(str);
        System.out.println(str2);
        Double d1=new Double(20.2);
        double d1new=d1;
        System.out.println(d1);
        System.out.println(d1new);
        Boolean b1=new Boolean(false);
        boolean b1new=b1;
        System.out.println(b1new);
        Long l1=new Long(20000l);
        long l1new=l1;
        System.out.println(l1);
        System.out.println(l1new);
        Character ch1=new Character('J');
        char ch1new=ch1;
        System.out.println(ch1);
        System.out.println(ch1new);

    }
}
