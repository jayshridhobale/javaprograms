package programing;

public class Demo37 {
    public static void main(String[] args) {
        int row=5;
        int line=3;


        for(int i=1;i<=row;i++)
        {
            for(int j=1;j<=line;j++)
            {

                System.out.print(i+"\t"+j+"\t");
            }


            System.out.println();
        }
    }
}
