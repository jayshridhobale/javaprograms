public class Demo62 {
    public static void main(String[] args) {
        int line=5;
        int star=5;
        for(int i=0; i<line;i++)
        {
            for(int j=0;j<star;j++)
            {

                    System.out.print(star+"\t");

            }
            star--;
            System.out.println();
        }
    }

}
