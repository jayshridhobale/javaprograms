public class Demo10 {
    public static void main(String[] args) {
        int star=5;
        int line=5;
        for (int i=0;i<star;i++)
        {
            for  (int j=0;j<line;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
